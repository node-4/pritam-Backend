exports.updateAddress = async (req, res, next) => {
        try {
                const data = await User.findOne({ _id: req.user._id, });
                if (data) {
                        const data1 = await Address.findById({ _id: req.params.id });
                        if (data1) {
                                if (req.body.coordinates) {
                                        req.body.location = { type: "Point", coordinates: req.body.coordinates || [0, 0], };
                                } else {
                                        req.body.location = data1.location;
                                }
                                let obj = {
                                        address: req.body.address ?? data1.address,
                                        street: req.body.street ?? data1.street,
                                        city: req.body.city ?? data1.city,
                                        state: req.body.state ?? data1.state,
                                        country: req.body.country ?? data1.country,
                                        pinCode: req.body.pinCode ?? data1.pinCode,
                                        name: req.body.name ?? data1.name,
                                        contactNumber: req.body.contactNumber ?? data1.contactNumber,
                                        addressType: req.body.addressType ?? data1.addressType, // Home, Work, Other
                                        email: req.body.email ?? data1.email,
                                        location: req.body.location
                                }
                                let update = await Address.findByIdAndUpdate({ _id: data1._id }, { $set: obj }, { new: true });
                                if (update) {
                                        return res.status(200).json({ status: 200, message: "Address update successfully.", data: update });
                                }
                        } else {
                                return res.status(404).json({ status: 404, message: "No data found", data: {} });
                        }
                } else {
                        return res.status(404).json({ status: 404, message: "No data found", data: {} });
                }
        } catch (error) {
                console.log(error);
                return res.status(501).send({ status: 501, message: "server error.", data: {}, });
        }
};
